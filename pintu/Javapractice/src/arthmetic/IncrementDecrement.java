package arthmetic;

public class IncrementDecrement {

	public static void main(String[] args) {
		//increment operator
		int a=5;
		a=a+1;
		System.out.println(a);//6
		a++;
		System.out.println(a);//7
		//decrement operator
		a=a-1;
		System.out.println(a);//6
		a--;
		System.out.println(a);//5	
				
	}

}
